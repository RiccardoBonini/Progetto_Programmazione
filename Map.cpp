Map::Map()
{
	width = 50;
	height = 10;

	wall = TextureManager::LoadTexture("sprite/wall2.png");
	terrain = TextureManager::LoadTexture("sprite/terrain.png");
	src.x = src.y = dest.x = dest.y = 0;
	src.h = src.w = dest.h = dest.w = 32;

	for (int i = 0; i < 20; i++) {
		for (int j = 0; j < 20; j++)
			map[i][j] = lvl[i][j];
	}
}


